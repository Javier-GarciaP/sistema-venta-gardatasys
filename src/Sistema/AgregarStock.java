    private void cargarSugerenciasNombre() {
        String[] tempNombre = productos.cargarSugerenciasNombre();
        ac.removeAllItems();
        for (int i = 0; i < tempNombre.length; i++) {
            ac.addItem(tempNombre[i]);
        }
    }
    
