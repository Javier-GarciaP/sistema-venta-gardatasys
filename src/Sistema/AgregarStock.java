    private void jb_guardar_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_guardar_agregarActionPerformed
        String nombre = jt_nombre_agregar.getText();
        double cantidad = Double.parseDouble(jt_cantidad_agregar.getText());
        productos.actualizarStock(nombre, cantidad);
        jt_nombre_agregar.setText("");
        jt_cantidad_agregar.setText("");
        JOptionPane.showMessageDialog(null, "Stock actualizado correctamente");
    }//GEN-LAST:event_jb_guardar_agregarActionPerformed

